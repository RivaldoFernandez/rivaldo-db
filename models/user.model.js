const mongoose = require('mongoose');

// Definici√≥n del esquema para usuarios
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true,
  },
  age: {
    type: Number,
    required: true,
    min: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Crear el modelo a partir del esquema
const User = mongoose.model('User', UserSchema);

module.exports = User;
